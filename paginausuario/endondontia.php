    <div class="container">
        <h2 class="text-center my-4">Dicas para Cuidados Endodônticos</h2>
        <ul class="list-group">
            <li class="list-group-item"><strong>Prevenção de Cáries:</strong> Evite cáries escovando os dentes regularmente com pasta de dente fluoretada, usando fio dental e fazendo consultas periódicas ao dentista. As cáries não tratadas são a principal causa de necessidade de tratamento de canal.</li>
            <li class="list-group-item"><strong>Cuide das suas restaurações:</strong> Se você tiver uma restauração dentária, verifique regularmente se ela está intacta. Se perceber qualquer dano, como rachaduras ou falhas, procure seu dentista para evitar que a infecção alcance a polpa.</li>
            <li class="list-group-item"><strong>Evite alimentos duros:</strong> Evite mastigar alimentos excessivamente duros, como balas e nozes, pois podem causar rachaduras no dente e danificar a polpa, resultando na necessidade de um tratamento de canal.</li>
            <li class="list-group-item"><strong>Sintomas de Infecção:</strong> Se você tiver dor intensa, inchaço ou sensibilidade prolongada a alimentos e bebidas quentes ou frias, isso pode ser sinal de uma infecção na polpa do dente. Procure um dentista imediatamente.</li>
            <li class="list-group-item"><strong>Após o tratamento de canal:</strong> Após realizar o tratamento de canal, evite colocar muita pressão no dente tratado até que ele esteja totalmente restaurado. Além disso, siga todas as instruções pós-operatórias fornecidas pelo seu dentista.</li>
        </ul>
