        <h2 class="text-center my-4">Dicas para Cuidados Periodontais</h2>
        <ul class="list-group">
            <li class="list-group-item"><strong>Tenha uma boa higiene bucal:</strong> Escove os dentes regularmente, usando uma escova macia e pasta de dente com flúor. Não se esqueça de escovar a linha da gengiva para evitar a acumulação de placa bacteriana.</li>
            <li class="list-group-item"><strong>Cuide das gengivas:</strong> Ao escovar os dentes, seja gentil com as gengivas. Escovar com muita força pode causar recessão gengival. Use escovas com cerdas macias para não danificar o tecido gengival.</li>
            <li class="list-group-item"><strong>Hidrate-se adequadamente:</strong> A produção de saliva é essencial para proteger as gengivas e os dentes contra bactérias. Beba muita água ao longo do dia para garantir que sua boca esteja sempre hidratada.</li>
            <li class="list-group-item"><strong>Alimente-se de forma equilibrada:</strong> Evite alimentos processados e ricos em açúcares que podem alimentar as bactérias nocivas à saúde periodontal. Priorize frutas, vegetais e alimentos ricos em vitaminas C e D para fortalecer as gengivas.</li>
            <li class="list-group-item"><strong>Considere tratamentos periodontais preventivos:</strong> Se você tiver histórico de doenças gengivais, converse com seu dentista sobre tratamentos preventivos, como limpezas profundas, que ajudam a remover a placa acumulada e evitam problemas mais sérios.</li>
        </ul>
