            <li class="list-group-item"><strong>Escovação regular:</strong> Ensine as crianças a escovar os dentes pelo menos duas vezes ao dia, com escova e pasta adequadas para a idade. A escovação deve ser supervisionada, especialmente nos primeiros anos.</li>
            <li class="list-group-item"><strong>Uso de fio dental:</strong> A partir do momento que as crianças tiverem dentes que se toquem, é importante introduzir o uso do fio dental para evitar o acúmulo de placa bacteriana entre os dentes.</li>
            <li class="list-group-item"><strong>Visite o dentista regularmente:</strong> A primeira visita ao dentista deve ocorrer quando o primeiro dente aparecer, para garantir que o desenvolvimento da boca da criança está indo bem e para prevenir problemas mais graves.</li>
            <li class="list-group-item"><strong>Evite alimentos açucarados:</strong> Alimentos e bebidas açucaradas podem causar cáries. Incentive uma alimentação saudável e o consumo moderado de doces.</li>
            <li class="list-group-item"><strong>Uso de protetores bucais:</strong> Se a criança pratica esportes, o uso de protetores bucais é fundamental para evitar traumas nos dentes e gengivas.</li>
