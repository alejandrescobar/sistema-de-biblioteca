    <div class="container">
        <h2 class="section-title text-center my-4">O Que São Implantes Dentários?</h2>
        <p class="text-center">Implantes dentários são pequenas estruturas de titânio que substituem as raízes dos dentes perdidos. Eles são colocados cirurgicamente no osso maxilar, criando uma base estável para dentes artificiais, como coroas, pontes ou próteses. O procedimento tem se tornado cada vez mais popular devido à sua durabilidade, estética e funcionalidade.</p>

        <h2 class="section-title text-center my-4">Vantagens dos Implantes Dentários</h2>
        <ul class="list-group">
            <li class="list-group-item"><strong>Estética Natural:</strong> O implante dentário proporciona uma aparência e função muito próximas aos dentes naturais.</li>
            <li class="list-group-item"><strong>Durabilidade:</strong> Os implantes são altamente duráveis, podendo durar por décadas quando bem cuidados.</li>
            <li class="list-group-item"><strong>Conforto:</strong> O implante melhora a mastigação, fala e conforto geral do paciente, sem a necessidade de próteses removíveis.</li>
            <li class="list-group-item"><strong>Preservação do Osso:</strong> O implante evita a perda óssea, que pode ocorrer com a falta de dentes, ajudando a manter a estrutura facial do paciente.</li>
        </ul>

        <h2 class="section-title text-center my-4">Como Funciona o Processo de Colocação do Implante?</h2>
        <div class="process-step">
            <h4>1. Avaliação Inicial e Planejamento</h4>
            <p>O primeiro passo é uma avaliação detalhada, que inclui exames radiológicos, para garantir que você tenha os requisitos necessários para receber o implante. O dentista também criará um plano personalizado para o seu caso.</p>
        </div>
        <div class="process-step">
            <h4>2. Colocação do Implante</h4>
            <p>O implante de titânio é colocado cirurgicamente no osso maxilar. Este processo é realizado sob anestesia local, garantindo que você não sinta dor durante o procedimento. O tempo de cirurgia é relativamente curto.</p>
        </div>
        <div class="process-step">
            <h4>3. Cicatrização e Integração Óssea</h4>
            <p>Após a colocação do implante, ocorre a fase de cicatrização, onde o osso e o implante se integram, um processo chamado osteointegração. Isso pode levar de 3 a 6 meses. Durante esse período, o paciente pode continuar com suas atividades normais.</p>
        </div>
        <div class="process-step">
            <h4>4. Colocação da Coroa ou Prótese</h4>
            <p>Depois que o implante estiver completamente integrado ao osso, é colocada a coroa ou prótese sobre o implante. Esse passo é feito com uma anestesia local, tornando o procedimento quase indolor. A coroa será personalizada para se ajustar perfeitamente ao seu sorriso.</p>
        </div>

        <h2 class="section-title text-center my-4">Adaptação Rápida e Quase Indolor</h2>
        <p class="text-center">O processo de adaptação aos implantes dentários é extremamente rápido e a maioria dos pacientes relata uma experiência confortável. Graças à tecnologia moderna e técnicas aprimoradas, o procedimento é praticamente indolor, e a recuperação geralmente ocorre em poucas semanas. O uso de anestesia local elimina qualquer desconforto durante a cirurgia, e os pacientes podem retomar suas atividades normais logo após o procedimento.</p>
    </div>

   

