        <h2 class="text-center my-4">Dicas para Cuidados Ortodônticos</h2>
        <ul class="list-group">
            <li class="list-group-item"><strong>Escove os dentes corretamente:</strong> Use uma escova macia e escove ao redor dos brackets e fios, removendo resíduos de alimentos e placa bacteriana. A escovação deve ser feita de forma cuidadosa, pelo menos três vezes ao dia.</li>
            <li class="list-group-item"><strong>Use fio dental regularmente:</strong> O uso do fio dental é crucial, pois ajuda a remover a placa bacteriana entre os dentes e os brackets, prevenindo cáries e doenças gengivais.</li>
            <li class="list-group-item"><strong>Evite alimentos duros e pegajosos:</strong> Alimentos como balas, pipoca, chicletes e caramelos podem danificar o aparelho ortodôntico. Opte por alimentos mais macios e evite mastigar coisas muito duras.</li>
            <li class="list-group-item"><strong>Visite seu ortodontista regularmente:</strong> Mantenha o acompanhamento com o ortodontista, realizando as consultas periódicas para ajustes do aparelho e monitoramento do progresso do tratamento.</li>
            <li class="list-group-item"><strong>Use os elásticos conforme orientação:</strong> Se o seu ortodontista prescrever o uso de elásticos, é fundamental seguir as orientações e usá-los corretamente para garantir a efetividade do tratamento.</li>
